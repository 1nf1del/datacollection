long long base(int b) {
	long long res = 0;
	for (int i = 0; i < n; ++i)
	{
		res += a[i] * bk[b][i];
	}
	return res;
}

void generate() {
	int tmp = 1;
	while(tmp < n - 1 && a[tmp] == 1) {
		a[tmp] = 0;
		tmp ++;
	}
	a[tmp] = 1;
}

