
public class intHolderTriple {
	int one;
	int two;
	int three;
	intHolderTriple(int One, int Two, int Three){
		one=One;
		two=Two;
		three=Three;
	}
	public void setOne(int One){
		one=One;
	}
	public void setTwo(int Two){
		two=Two;
	}
	public void setThree(int Three){
		three=Three;
	}
	public void set(int One, int Two, int Three){
		one=One;
		two=Two;
		three=Three;
	}
}
